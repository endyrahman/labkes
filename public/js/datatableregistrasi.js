dtregistrasiklinik.on('click', 'td.details-control', function () {
    var tr = $(this).closest('tr');
    var row = dtregistrasiklinik.row(tr);

    if (row.child.isShown()) {
        // This row is already open - close it
        row.child.hide();
        tr.removeClass('shown');
    } else {
        // Open this row
        row.child(dtchildregistrasiklinik(tr.data('child-value'))).show();
        tr.addClass('shown');
    }
});

dtregistrasiklinik.on('order.dt search.dt', function() {
    dtregistrasiklinik.column(0, {
        search: 'applied',
        order: 'applied'
    }).nodes().each(function(cell, i) {
        cell.innerHTML = i + 1;
    });
}).draw();

